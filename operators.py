## Arithmetic operators

x = 10

y = 20

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x // y)
print(x % y)
print(x ** y)

## Logical operators
a = True
b = False

print(a and b)
print(a or b)

## Comparison Operators
print(" -------   Comparison Operators --------- ")
print(x > y)
print(x < y)
print(x >= y)
print(x <= y)
print(x == y)
print(x != y)

# Association operators
print(" -------   Association Operators --------- ")
l = [1, 2, 3, 4, "aman", 2.5, True, [2, 3]]

print(3 in l)
print(3 not in l)

